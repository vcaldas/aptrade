from typing import Union
from aptrade.main import welcome
from fastapi import FastAPI

app = FastAPI()


@app.get("/")
def read_root():
    return {"Hello": "World"}


@app.get("/items/{item_id}")
def read_item(item_id: int, q: Union[str, None] = None):
    return {"item_id": item_id, "q": q}

@app.get("/welcome")
def api_welcome():
    message = welcome()
    return {"message": message}